	$zone_codes_data = array();
		
	$zone_codes_array = array();
		
	$pp_zone_codes_data = array();
		
	if (!empty($pp_express_zones) && is_array($pp_express_zones)) {
		foreach ($pp_express_zones as $pp_country_name => $pp_zones) {
			if (!empty($pp_express_zones[$pp_country_name])) {
				$zone_codes = array_keys($pp_express_zones[$pp_country_name]);
					
				if (!empty($zone_codes)) {
					$implode[] = "LCASE(TRIM(`c`.`name`)) = '" . strtolower($pp_country_name) . "'";
						
					$pp_country_names[] = $pp_country_name;
						
					foreach ($zone_codes as $zone_code) {
						if (!empty($pp_express_zones[$pp_country_name][$zone_code])) {
							$pp_code = array_keys($pp_express_zones[$pp_country_name][$zone_code])[0];							
								
							$zone_codes_data[] = "UCASE(TRIM(`z`.`code`)) = '" . strtoupper($zone_code) . "'";
								
							$zone_codes_array[] = $zone_code;
								
							$pp_zone_codes_data[$pp_country_name][$zone_code] = $pp_code;
						}
					}
				}
			}
		}
	}
		
	return array('implode'					=> $implode,
				 'pp_country_names'			=> $pp_country_names,
				 'zone_codes_array'			=> $zone_codes_array,
				 'zone_codes_data'			=> $zone_codes_data,
				 'pp_zone_codes_data'		=> $pp_zone_codes_data,
				);
}
